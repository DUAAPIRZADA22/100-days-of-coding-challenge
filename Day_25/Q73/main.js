"use strict";
//  Assigning and Updating Variables:
// this function shows how to assign and update variable values
function updateVariable() {
    let favNumber = 25; // initially assigns the value 25
    console.log("Initial value: ", favNumber);
    favNumber = 7; // updates the value to 7
    console.log("Updated Value: ", favNumber);
}
// calling the function
updateVariable();
// we assign a value to a variable and then update it, showing how values can change.
