"use strict";
//  Converting to Upper and Lower Case:
function convertUpperLower(myName) {
    let upper = myName.toLowerCase(); // to lower case
    let lower = myName.toUpperCase(); // to upper case
    console.log(`Upper: ${upper}`);
    console.log(`Lower: ${lower}`);
}
;
convertUpperLower("DuaA PIrZaDa");
